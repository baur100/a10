package NewHM9;

public class Cars {
    public String brand;
    public int year;
    public int price;

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public Cars(String brand, int year, int price) {
        this.brand = brand;
        this.year = year;
        this.price = price;




    }
}

